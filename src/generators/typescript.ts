function generateProgram(ctx: NormalizedContext): string {
  const lines: string[] = [];
  const hasEffects = contextHasEffects(ctx);

  lines.push("// Auto-generated by cli-contracts. Do not edit.");
  lines.push('import { Command } from "commander";');
  if (hasEffects) {
    lines.push('import { commandDefinitions, deriveCommandPolicy } from "./policy.js";');
  }
  lines.push("");

  // Build the CommandHandlers interface
  lines.push("export interface CommandHandlers {");
  for (const cs of ctx.commandSets) {
    for (const cmd of cs.commands) {
      const handlerName = toCamelCase(cmd.id);
      const sig = buildHandlerSignature(cmd);
      lines.push(`  ${handlerName}: ${sig};`);
    }
  }
  lines.push("}");
  lines.push("");

  // Build createProgram function
  lines.push("export function createProgram(");
  lines.push("  handlers: CommandHandlers,");
  lines.push("  version: string,");
  lines.push("): Command {");
  lines.push("  const program = new Command();");

  // Assumes single command set for program name; use first
  const primaryCs = ctx.commandSets[0];
  lines.push(`  program`);
  lines.push(`    .name(${JSON.stringify(primaryCs.executable)})`);
  lines.push(`    .version(version, "-V, --version")`);
  lines.push(
    `    .description(${JSON.stringify(primaryCs.summary ?? ctx.info.description ?? "")});`,
  );
  lines.push("");

  for (const opt of primaryCs.globalOptions) {
    const skipBuiltins = ["version", "help"];
    if (skipBuiltins.includes(opt.name)) continue;
    const optFlag = buildOptionFlag(opt);
    const desc = JSON.stringify(opt.description ?? "");
    const def = opt.schema?.default;
    if (def !== undefined) {
      const defValue = commanderDefaultValue(def, opt.schema?.type);
      lines.push(
        `  program.option(${JSON.stringify(optFlag)}, ${desc}, ${defValue});`,
      );
    } else {
      lines.push(
        `  program.option(${JSON.stringify(optFlag)}, ${desc});`,
      );
    }
  }

  if (hasEffects) {
    lines.push(
      `  program.option("--introspect", "Output derived policy as JSON without executing the command");`,
    );
  }
  lines.push("");

  // Commands
  for (const cs of ctx.commandSets) {
    for (const cmd of cs.commands) {
      generateProgramCommand(cmd, lines, hasEffects);
    }
  }

  lines.push("  return program;");
  lines.push("}");
  lines.push("");

  return lines.join("\n");
}

function buildHandlerSignature(cmd: NormalizedCommand): string {
  const params: string[] = [];

  for (const arg of cmd.arguments) {
    const type = arg.variadic ? "string[]" : "string | undefined";
    params.push(`${safeIdentifier(arg.name)}: ${type}`);
  }

  if (cmd.options.length > 0) {
    const optFields = cmd.options.map((o) => {
      const type = o.schema?.type === "boolean" ? "boolean" : "string";
      return `${toCamelCase(o.name)}?: ${type}`;
    });
    params.push(`options: { ${optFields.join("; ")} }`);
  } else {
    params.push("options: Record<string, never>");
  }

  params.push("parentOpts: Record<string, unknown>");

  return `(${params.join(", ")}) => Promise<void>`;
}

function generateProgramCommand(
  cmd: NormalizedCommand,
  lines: string[],
  hasEffects: boolean = false,
): void {
  const handlerName = toCamelCase(cmd.id);
  const cmdPath = cmd.path.join(" ");

  lines.push(`  program`);
  lines.push(`    .command(${JSON.stringify(cmdPath)})`);
  lines.push(`    .description(${JSON.stringify(cmd.summary)})`);

  // Arguments
  for (const arg of cmd.arguments) {
    const token = arg.variadic
      ? arg.required ? `<${arg.name}...>` : `[${arg.name}...]`
      : arg.required ? `<${arg.name}>` : `[${arg.name}]`;
    const desc = JSON.stringify(arg.description ?? "");
    lines.push(`    .argument(${JSON.stringify(token)}, ${desc})`);
  }

  for (const opt of cmd.options) {
    const flag = buildOptionFlag(opt);
    const desc = JSON.stringify(opt.description ?? "");
    const def = opt.schema?.default;
    if (def !== undefined) {
      const defValue = commanderDefaultValue(def, opt.schema?.type);
      lines.push(
        `    .option(${JSON.stringify(flag)}, ${desc}, ${defValue})`,
      );
    } else {
      lines.push(`    .option(${JSON.stringify(flag)}, ${desc})`);
    }
  }

  const argSafeNames = cmd.arguments.map((a) => safeIdentifier(a.name));
  const actionParams = [...argSafeNames];
  actionParams.push("opts");
  actionParams.push("cmd");

  lines.push(
    `    .action(async (${actionParams.join(", ")}) => {`,
  );

  if (hasEffects) {
    lines.push("      const globalOpts = cmd.optsWithGlobals();");
    lines.push("      if (globalOpts.introspect) {");
    lines.push(`        const policy = deriveCommandPolicy(${JSON.stringify(cmd.id)}, opts);`);
    lines.push("        console.log(JSON.stringify(policy, null, 2));");
    lines.push("        return;");
    lines.push("      }");
  }

  const callArgs = [
    ...argSafeNames,
    cmd.options.length > 0 ? "opts" : "{}",
    hasEffects ? "globalOpts" : "cmd.optsWithGlobals()",
  ];
  lines.push(
    `      await handlers.${handlerName}(${callArgs.join(", ")});`,
  );
  lines.push("    });");
  lines.push("");
}

function commanderDefaultValue(
  def: unknown,
  schemaType?: string | string[],
): string {
  if (typeof def === "boolean") return JSON.stringify(def);
  if (typeof def === "string") return JSON.stringify(def);
  // Commander expects string defaults for non-boolean options
  return JSON.stringify(String(def));
}

function contextHasEffects(ctx: NormalizedContext): boolean {
  for (const cs of ctx.commandSets) {
    for (const cmd of cs.commands) {
      if (cmd.extensions.effects) return true;
      for (const opt of cmd.options) {
        if (opt.effects) return true;
      }
    }
  }
  return false;
}

function generatePolicyModule(ctx: NormalizedContext): string {
  const lines: string[] = [];
  lines.push("// Auto-generated by cli-contracts. Do not edit.");
  lines.push('import { derivePolicy, isOptionActive } from "cli-contracts/policy";');
  lines.push('import type { DerivedPolicy, IntrospectionResult } from "cli-contracts/policy";');
  lines.push("");
  lines.push("export type { DerivedPolicy, IntrospectionResult };");
  lines.push("");

  lines.push("export const commandDefinitions = {");
  for (const cs of ctx.commandSets) {
    for (const cmd of cs.commands) {
      const raw = cmd as unknown as Record<string, unknown>;
      const cmdDef: Record<string, unknown> = {};

      const cmdEffects = cmd.extensions.effects;
      if (cmdEffects) {
        cmdDef.effects = cmdEffects;
      }

      const optionDefs: Record<string, unknown>[] = [];
      for (const opt of cmd.options) {
        const optDef: Record<string, unknown> = {
          name: opt.name,
        };
        if (opt.schema) optDef.schema = opt.schema;
        if (opt.file) optDef.file = opt.file;
        if (opt.effects) optDef.effects = opt.effects;
        if (opt.repeatable) optDef.repeatable = opt.repeatable;
        optionDefs.push(optDef);
      }
      if (optionDefs.length > 0) {
        cmdDef.options = optionDefs;
      }

      const envDef: Record<string, unknown> = {};
      if (cs.env) {
        for (const [envName, envVar] of Object.entries(cs.env)) {
          if (envVar.sensitive) {
            envDef[envName] = { sensitive: true };
          }
        }
      }
      if (Object.keys(envDef).length > 0) {
        cmdDef.env = envDef;
      }

      lines.push(`  ${JSON.stringify(cmd.id)}: ${JSON.stringify(cmdDef, null, 2).replace(/\n/g, "\n  ")},`);
    }
  }
  lines.push("} as const;");
  lines.push("");

  lines.push(
    "export function deriveCommandPolicy(",
  );
  lines.push("  commandId: string,");
  lines.push("  optionValues: Record<string, unknown>,");
  lines.push("): IntrospectionResult {");
  lines.push("  const def = commandDefinitions[commandId as keyof typeof commandDefinitions];");
  lines.push('  if (!def) throw new Error(`Unknown command: ${commandId}`);');
  lines.push("");
  lines.push("  const cmdDef = def as { effects?: unknown; options?: readonly { name: string; schema?: { type?: string }; file?: unknown; effects?: unknown; repeatable?: boolean }[]; env?: Record<string, { sensitive?: boolean }> };");
  lines.push("  const options: Record<string, { value: unknown; specified: boolean; definition: { name: string; schema?: { type?: string }; file?: { mode: string }; effects?: unknown; repeatable?: boolean } }> = {};");
  lines.push("  const activeOptions: string[] = [];");
  lines.push("");
  lines.push("  for (const optDef of cmdDef.options ?? []) {");
  lines.push("    const value = optionValues[optDef.name];");
  lines.push("    const specified = optDef.name in optionValues && value !== undefined;");
  lines.push("    options[optDef.name] = { value, specified, definition: optDef as never };");
  lines.push("    if (isOptionActive(optDef as never, value, specified)) {");
  lines.push("      activeOptions.push(optDef.name);");
  lines.push("    }");
  lines.push("  }");
  lines.push("");
  lines.push("  const policy = derivePolicy({");
  lines.push("    commandId,");
  lines.push("    commandEffects: cmdDef.effects as never,");
  lines.push("    options,");
  lines.push("    env: cmdDef.env as never,");
  lines.push("  });");
  lines.push("");
  lines.push("  return { command: commandId, activeOptions, policy };");
  lines.push("}");
  lines.push("");

  return lines.join("\n");
}

function buildOptionFlag(opt: Option): string {
  const parts: string[] = [];
  if (opt.aliases && opt.aliases.length > 0) {
    parts.push(`-${opt.aliases[0]}`);
  }
  const isBoolean = opt.schema?.type === "boolean";
  if (isBoolean) {
    parts.push(`--${opt.name}`);
  } else {
    const valueName = opt.valueName ?? "value";
    if (opt.repeatable) {
      parts.push(`--${opt.name} <${valueName}...>`);
    } else {
      parts.push(`--${opt.name} <${valueName}>`);
    }
  }
  return parts.join(", ");
}
